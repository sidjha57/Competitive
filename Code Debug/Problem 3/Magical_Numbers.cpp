/*
Let's denote a function f(x) in such a way: we subtract 1 to x, then, while there is at least one trailing zero in the resulting number, we remove that zero.
we end the operation when x equals 0'
For example,

f(35)=10: 35-1=34->33->32->31->30->3->2->1->0
f(8)=9: 8-1=7->6->5->4->3->2->1->0;

We say that some number y is reachable from x if we can apply function f to x some (possibly zero) times so that we get y as a result. For example, 109 is reachable from 1092 because f(f(f(1092)))=f(f(1091))=f(1090)=109; and any number is reachable from itself.

You are given a number n; your task is to count how many different numbers are reachable from n. */
ll num (ll n) {
    n -= 1;
    while (n%10 == 0 && n > 0) n /= 10;
    return n;
}
void solve() {
    ll n; cin >> n; 
    set<ll> dis;
    while (dis.find(n) == dis.end() && n > 0) {
        dis.emplace(n);
        n = num(n);
    }
    cout << dis.size() << "\n";
}

